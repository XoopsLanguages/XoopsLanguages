<h1>راهنمای استفاده از ماژول</h1>
<p>
	L'utilizzo di questo modulo si pu&ograve; sintetizzare nei seguenti passi che &egrave; necessario eseguire in ordine per un funzionamento corretto del modulo.
</p>
<h2>1. verifica editor correttamente installati</h2>
<p>
	The first tab &quot;Check Editors&quot; presenta l'elenco di tutti gli editor che il modulo pu&ograve; analizzare (attenzione: questo non &egrave; l'elenco di tutti i possibili editor installati ma solo l'elenco degli editor ricunosciuti da questo modulo).
</p>
<p>
	Se l'editor elencato &egrave; correttamente presente/installato 
	<img src="../images/icons/on.gif" />
	sul vostro sito &egrave; possibile testarlo (pulsante &quot;Prova l'editor&quot; 
	<img src="../images/icons/test.gif" />
	) o aggiungerlo all'elenco degli editor utilizzabili da questo modulo 
	<img src="../images/icons/add.gif" />
	; cliccare su questo pulsante per aggiungere l'editor all'elenco presente nel tab &quot;Elenco editor installati&quot;.
</p>
<h2>2. scelta degli editor utilizzabili, scelta dell'editor di default</h2>
<p>
	The second tab &quot;Editors List&quot; presenta l'elenco degli editor che questo modulo potr&agrave; utilizzare.
</p>
<p>
	&Egrave; possibile modificare tutti i parametri (questo per consentire la massima libert&agrave; a chi amministra il modulo, &egrave; anche possibile 'fare danni'), scegliere il &quot;peso&quot; (0,zero pi&ugrave; importante), cio&egrave; l'editor che avr&agrave; la priorit&agrave; nel caso un utente appartenesse a diversi gruppi con editor selezionati differenti (vedi passo successivo).
</p>
<p>
	&Egrave; possibile scegliere un editor di default.
</p>
<p>
	Se desiderate aggiungere altri editor (non elencati nel tab &quot;Editor Installabili&quot; ma correttemente installati) potete aggiungerli qui.
</p>
<h2>3. definizione, per ogni gruppo e modulo, dell'editor utilizzabile</h2>
<p>
	The third tab &quot;Modules &amp; Groups Array&quot; presenta la tabella &quot;MODULEs, GROUPs, URLs, PAGEs, TEXTAREAs array&quot;. Questa tabella permette di scegliere, per ogni gruppo e modulo, l'editor da utilizzare.
</p>
